package co.com.ges.util;

public class Meses {
	private String mes;
	private int numMes;

	public Meses(String mes, int numMes) {
		this.mes = mes;
		this.numMes = numMes;
	}

	public String getMes() {
		return this.mes;
	}

	public void setMes(String mes) {
		this.mes = mes;
	}

	public int getNumMes() {
		return this.numMes;
	}

	public void setNumMes(int numMes) {
		this.numMes = numMes;
	}

}
